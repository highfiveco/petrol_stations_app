package co.highfive.petrolstation.models;

public class Fund {
    private String id;
    private String company_id;
    private String credit;
    private String currency;
    private String depit;
    private String open_date;
    private String close_date;
    private String name;
    private String max_id;
    private String amount_close;
    private String name_currency;
    private String user_name_close;
    private String fund_close_to;
    private String url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCompany_id() {
        return company_id;
    }

    public void setCompany_id(String company_id) {
        this.company_id = company_id;
    }

    public String getCredit() {
        return credit;
    }

    public void setCredit(String credit) {
        this.credit = credit;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getDepit() {
        return depit;
    }

    public void setDepit(String depit) {
        this.depit = depit;
    }

    public String getOpen_date() {
        return open_date;
    }

    public void setOpen_date(String open_date) {
        this.open_date = open_date;
    }

    public String getClose_date() {
        return close_date;
    }

    public void setClose_date(String close_date) {
        this.close_date = close_date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMax_id() {
        return max_id;
    }

    public void setMax_id(String max_id) {
        this.max_id = max_id;
    }

    public String getAmount_close() {
        return amount_close;
    }

    public void setAmount_close(String amount_close) {
        this.amount_close = amount_close;
    }

    public String getName_currency() {
        return name_currency;
    }

    public void setName_currency(String name_currency) {
        this.name_currency = name_currency;
    }

    public String getUser_name_close() {
        return user_name_close;
    }

    public void setUser_name_close(String user_name_close) {
        this.user_name_close = user_name_close;
    }

    public String getFund_close_to() {
        return fund_close_to;
    }

    public void setFund_close_to(String fund_close_to) {
        this.fund_close_to = fund_close_to;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
